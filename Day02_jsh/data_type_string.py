'''
* 탈출 문자(escape _opcode)
1. \n: 줄 개행을 실행
2. \t: 탭 명령을 실행
3. \r: 캐리지 리턴(커서를 왼쪽으로 이동)
4. \\: 문자 \를 표현
5. \': 문자 '를 표현
6. \": 문자 "를 표현

'''
print("----------------")
s6 = "안녕~~~!\n잘가!!"
print(s6)

s7 = "안녕~~~!\t\t\t잘가!!"
print(s7)


file1 = "C:\\temp\\new.jpg" # \를 문자로 사용.
print(file1)

