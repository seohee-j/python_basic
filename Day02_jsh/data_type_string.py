
'''
* 문자열(Strings)
- 문자열(str)은 일련의 문자들을 따옴표("", '')로 감싸 나열한 문자데이터의 집합형태이다.
- 따옴표 안에 어던 데이터의 값을 넣더라도 모두 문자열로 인식한다.
- 전세계의 모든 문자를 저장할 수 있으며 저장 길이에 제한이 없다.
'''

s1 = "안녕하세요!"
print(type(s1))

s2 = '잘가요~'
print(type(s2))

s3 = "나는 그에게 '반가워'라고 말했다."
print(s3)

s4 = "Let's go!"
print(s4)

s5 = "Let's go! \"together\""
print(s5)
