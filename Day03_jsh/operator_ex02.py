'''
* 대입 연산자(=, +=, -=, *=, /=, ...)
- 변수에 값을 대입할 때 사용하는 연산자입니다.
'''
a = 5
b = 5

a = a + 1 # a += 1
print(a)

a += 3 # a = a + 3
print(a)

a -= 5
print(a)

b =+ 3  # b = +3
print(b)

print("-" * 40)
a *= 7 # a: 28
print(a)
a //= 2  # a: 14
print(a)
a %= 3 # a: 2
print(a)
a **= 4 # a: 16
print(a)

c = 0
c += 2 # c = c + 2

